public class StringExercise {
    public static void main(String[] args) {
        String sen = "We don't need no education";
        String We = "We don't need no thought control";
        System.out.println(sen);
        System.out.println(We);
        String j = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(j);
        String k = "In windows, the main drive is usually C:\\";
        System.out.println(k);
        String l = "I can do backslashes \\, double backslashes \\\\,\n";
        String m = "and the amazing triple backslash \\\\\\!";
        System.out.println(l);
        System.out.println(m);

    }
}
